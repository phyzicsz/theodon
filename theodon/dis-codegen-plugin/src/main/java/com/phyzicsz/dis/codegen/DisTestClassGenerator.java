package com.phyzicsz.dis.codegen;

import com.phyzicsz.dis.datamodel.api.DisClass;
import com.squareup.javapoet.ClassName;
import com.squareup.javapoet.JavaFile;
import com.squareup.javapoet.MethodSpec;
import com.squareup.javapoet.TypeSpec;
import java.io.File;
import java.io.IOException;
import java.nio.file.Path;
import javax.lang.model.element.Modifier;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 *
 * @author phyzicsz
 */
public class DisTestClassGenerator {

    private static final Logger LOGGER = LoggerFactory.getLogger(DisInterfaceGenerator.class);
    
    private JavaFile javaFile;

    public DisTestClassGenerator generate(String javaPackage, DisClass idl) {

        TypeSpec.Builder mainBuilder = TypeSpec.classBuilder(idl.getName() + "Test")
                .addModifiers(Modifier.PUBLIC)
                .addJavadoc(idl.getComment());
        
        if (null != idl.getIsAbstract() && idl.getIsAbstract()) {
            
            javaFile = JavaFile.builder(javaPackage, mainBuilder.build())
                    .addFileComment(insertHeader(idl.getComment()))
                    .build();

            return this;
        }

        MethodSpec constructor = TestMethodGenerator.constructor();
        MethodSpec wireline = TestMethodGenerator.wirelineSizeTest(idl);
        MethodSpec pduType = TestMethodGenerator.pduTypeTest(idl);

        mainBuilder.addMethod(constructor)
                .addMethod(wireline)
                .addMethod(pduType);

        ClassName assertEquals = ClassName.get("org.junit.jupiter.api", "Assertions");
        javaFile =  JavaFile.builder(javaPackage, mainBuilder.build())
                .addFileComment(insertHeader(idl.getComment()))
                .addStaticImport(assertEquals, "assertEquals")
                .build();
        
        return this;
    }

    private String insertHeader(String content) {

        StringBuilder sb = new StringBuilder();
        sb.append("/**")
                .append("\n")
                .append("* Autogenerated by dis-codegen")
                .append("\n")
                .append("*")
                .append("\n")
                .append("DO NOT EDIT DIRECTLY")
                .append("\n")
                .append("*/")
                .append("\n")
                .append(content);
        return sb.toString();
    }

    public DisTestClassGenerator writeClassFile(File outputPath) throws IOException {
        LOGGER.info("Writing file {}", javaFile.toJavaFileObject().getName());
        Path file = outputPath.toPath();
        file.toFile().mkdirs();
        javaFile.writeTo(file);
        return this;
    }

}
